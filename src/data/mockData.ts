const getUpcomingWeekday = (targetDay: number) => {
  const currentDay = today.getDay();
  const daysUntilTarget = (targetDay - currentDay + 7) % 7 || 7;
  return format(addDays(today, daysUntilTarget), 'yyyy-MM-dd');
};
